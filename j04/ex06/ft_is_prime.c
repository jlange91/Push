int		ft_is_prime(int nb)
{
	int i;
	int y;

	i = 0;
	y = 2;
	while (y <= nb)
	{
		if ((nb % y) == 0)
			i++;
		y++;
	}
	if (i == 1)
		return (1);
	else
		return (0);
}
